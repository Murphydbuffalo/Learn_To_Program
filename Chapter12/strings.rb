#You can use [] along with string variables to return the "character code" of the letter in that position in the string.  (Letters have corresponding number values).
#You can then use the ? before a letter to find its code.  
#Finally, you can use .chr follwing a number to find its corresponding letter/character/number.

puts 'Dan'[1]
puts ?a
puts ?D
puts 55.chr
